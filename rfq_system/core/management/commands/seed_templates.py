class Command(BaseCommand):
    help = "Создаёт (или обновляет) русские email-шаблоны."

    def handle(self, *args, **options):
        for t in TEMPLATES:
            EmailTemplate.objects.update_or_create(
                slug=t["slug"],
                defaults={"subject": t["subject"], "body": t["body"]},
            )
        self.stdout.write(self.style.SUCCESS("Email-шаблоны созданы/обновлены."))
